import math
angulo = float(input('Me informe um angulo: '))
seno = math.sin (math.radians(angulo))
print('O angulo {} em Seno é de {:.2f}'.format(angulo, seno))
conseno = math.cos (math.radians(angulo))
print('O angulo {} em Conseno é de {:.2f}'.format(angulo, conseno))
tangente = math.tan (math.radians(angulo))
print('O angulo {} em Tangente é de {:.2f}'.format(angulo, tangente))
